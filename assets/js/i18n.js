window.getLang = function () {
  return localStorage.getItem("selected-lang") || "en";
};

window.setLang = function (lang) {
  localStorage.setItem("selected-lang", lang);
  document.documentElement.setAttribute("lang", lang);
  window.applyI18n();
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang: lang } }));
};

window.t = function (key) {
  var lang = window.getLang();
  var pack = window.I18N[lang] || window.I18N.en;
  return pack[key] || window.I18N.en[key] || key;
};

window.applyI18n = function () {
  var lang = window.getLang();
  document.documentElement.setAttribute("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var key = el.getAttribute("data-i18n");
    var value = window.t(key);
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = value;
    } else {
      el.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
    el.placeholder = window.t(el.getAttribute("data-i18n-placeholder"));
  });

  document.querySelectorAll("[data-i18n-aria]").forEach(function (el) {
    el.setAttribute("aria-label", window.t(el.getAttribute("data-i18n-aria")));
  });

  var toggle = document.getElementById("lang-button");
  if (toggle) {
    toggle.textContent = window.t("lang.label");
  }
};

document.addEventListener("DOMContentLoaded", function () {
  window.applyI18n();
  var toggle = document.getElementById("lang-button");
  if (toggle) {
    toggle.addEventListener("click", function () {
      var next = window.getLang() === "en" ? "tr" : "en";
      window.setLang(next);
    });
  }
});
